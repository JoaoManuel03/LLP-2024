    printf("3. Verifique se há semáforos ou sinais de trânsito.\n");

    // 4. Espere até que o sinal esteja verde ou até que não haja trânsito
    printf("4. Espere até que o sinal esteja verde ou até que não haja trânsito.\n");

    // 5. Olhe para a esquerda, direita e novamente para a esquerda antes de atravessar
    printf("5. Olhe para a esquerda, direita e novamente para a esquerda antes de atravessar.\n");

    // 6. Atravessar a rua com calma e atenção
    printf("6. Atravessar a rua com calma e atenção.\n");

